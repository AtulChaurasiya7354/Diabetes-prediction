import numpy as np
import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

df = pd.read_csv("diabetes.csv")

df.head()

df.info()

df.isnull().sum()

# Check skewness

df['Outcome'].skew()   # nearest of zero (this data is Slightly left skewed)

sns.histplot(df['Outcome'],kde=True)

# Count plot for checking class Imbalnce ->difference between your categorical columns
# if categorical values - 2 value -> Binary classification
# more than 2 - value -> Multiclass classification

sns.countplot(x='Outcome', data=df)

sns.boxplot(x = df['Outcome'])

df['Outcome'].value_counts().plot(kind= 'bar')

df['Outcome'].value_counts().plot(kind= 'pie')

# corr = df.corr()

sns.heatmap(df.corr(),annot=True)

# Split Columns for traning and testing
X = df.drop('Outcome',axis=1)
y = df['Outcome']

# Do we need to use StandardScaler here ?
# -> We use StandarScaler when we are working on distance based algorithem
# -> we we are working on Probability based or Tree based alogorithms we Do not use this

# train test split
from sklearn.model_selection import train_test_split

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.33, random_state=42)

# Boosting Techniques
# 1. Adaboost
# 2. XGboost
# 3. GradientBoost

# 1. Adaboost
from sklearn.ensemble import AdaBoostClassifier

Adaboost_model = AdaBoostClassifier(
    n_estimators=50,  # weak learners
    learning_rate=1.0,
    random_state=None
)

# train on Adaboost Model
Adaboost_model.fit(X_train,y_train)

# Prediction on unseen data or X_test
Adaboost_pred = Adaboost_model.predict(X_test)

from sklearn.metrics import accuracy_score
accuracy_score(y_test,Adaboost_pred)

# trainin accuracy
accuracy_score(y_train,Adaboost_model.predict(X_train))

"""XGboost **Model**"""

# Make a model of XGboost

from xgboost import XGBClassifier

XGboost_model = XGBClassifier(
    n_estimators=10,
    learning_rate=0.05,
    random_state=42,
    max_depth=4,
)

# Train XGboost
XGboost_model.fit(X_train,y_train)

XGboost_pred = XGboost_model.predict(X_test)

accuracy_score(y_test,XGboost_pred)

# training accuracy
accuracy_score(y_train,XGboost_model.predict(X_train))  # Overfitting -> training accuracy comes is : 1.0  -> High Variance

"""**Gradientboost model**"""

# Train predict evaluate Gradientboost model

from sklearn.ensemble import GradientBoostingClassifier

Gradient_model = GradientBoostingClassifier(
    n_estimators=5,
    learning_rate=0.05,
    random_state=42,
    max_depth=2
)

Gradient_model.fit(X_train,y_train)

Gradient_model_pred = Gradient_model.predict(X_test)

accuracy_score(y_test,Gradient_model_pred)

accuracy_score(y_train,Gradient_model.predict(X_train))

#

from sklearn.naive_bayes import GaussianNB

naive_model = GaussianNB()

naive_model.fit(X_train,y_train)

naive_model_pred = naive_model.predict(X_test)

accuracy_score(y_test,naive_model_pred)

accuracy_score(y_train,naive_model.predict(X_train))

"""**Testing on Logistic regression**"""

from sklearn.linear_model import LogisticRegression

logistic_model = LogisticRegression()

logistic_model.fit(X_train,y_train)

accuracy_score(y_test,logistic_model.predict(X_test))

accuracy_score(y_train,logistic_model.predict(X_train))



"""**Testing on Knn**"""

from sklearn.neighbors import KNeighborsClassifier

knn_model = KNeighborsClassifier()

knn_model.fit(X_train,y_train)

accuracy_score(y_test,knn_model.predict(X_test))

accuracy_score(y_train,knn_model.predict(X_train))

